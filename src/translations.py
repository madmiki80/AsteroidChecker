def set_locale(locale):
    global _LOCALE
    if locale in T:
        _LOCALE = locale


def get_locale():
    return _LOCALE


def t(key, **kwargs):
    text = T.get(_LOCALE, T["en"]).get(key, T["en"].get(key, key))
    if kwargs:
        return text.format(**kwargs)
    return text


def translate_label(label):
    if label in ("HIGH", "MEDIUM", "LOW", "NONE", "HIGH_RISK"):
        return label
    return label


